# Episode 092: Final Mock Exam 2 (The White Rivals) - Integrated Review

## [WSET L1 Practice Question]

Q. Which comparison between Chardonnay and Sauvignon Blanc is the most accurate?

A. Chardonnay always has much higher acidity than Sauvignon Blanc.
B. Sauvignon Blanc is primarily aged in oak to produce vanilla and butter flavors.
C. Chardonnay produces various styles depending on climate and winemaking, while Sauvignon Blanc is characterized by intense herbaceous aromas.
D. Both varieties can only be grown in hot regions.

---

## [Answer & Explanation]

Answer: C. Chardonnay produces various styles depending on climate and winemaking, while Sauvignon Blanc is characterized by intense herbaceous aromas.

Explanation:
- Chardonnay: A versatile variety that can be lean and zesty or rich and buttery depending on oak and climate.
- Sauvignon Blanc: Known for its high acidity and distinctive 'herbaceous' (grass, bell pepper) aromas, usually avoiding oak to keep its freshness.

---

## [Expert Concept]

The White Wine Duel:

1.  Chardonnay (The Chameleon): Apple, Citrus (Cool) -> Peach, Pineapple (Warm). Often oaked.
2.  Sauvignon Blanc (The Personality): Grass, Bell Pepper, Passion fruit. Usually unoaked.
3.  Acidity: Sauvignon Blanc generally has higher perceived acidity than Chardonnay.

---

## [Exam Traps & Success Tips]

[Trap]: Don't think "all white wines are the same." Sauvignon Blanc's 'Green' aromas are its unique ID.
[Tip]: If the exam mentions 'Vanilla' and 'White Wine,' think Oaked Chardonnay.

