Q. Which of the following wine styles is incorrectly paired with its recommended serving temperature?

A. Sweet wine (Sauternes) - Well-chilled (6~8°C)
B. Sparkling wine (Champagne) - Well-chilled (6~10°C)
C. Full-bodied red wine (Cabernet Sauvignon) - Lightly chilled (10~12°C)
D. Light-bodied white wine (Pinot Grigio) - Chilled (7~10°C)

---

## [Answer & Explanation]

Answer: C. Full-bodied red wine (Cabernet Sauvignon) - Lightly chilled (10~12°C)

Explanation:
- Full-bodied reds should be served at room temperature (15~18°C) to allow their complex aromas to open up.
- 10~12°C is too cold for bold reds; it's better suited for light-bodied reds (like Pinot Noir) or full-bodied whites.

---

## [Expert Concept]

The Temperature Guide:

1.  6~10°C (Well-chilled): Sweet & Sparkling. (Keeps them refreshing)
2.  7~10°C (Chilled): Light/Medium Whites. (Enhances zest)
3.  10~13°C (Lightly chilled): Full Whites & Light Reds.
4.  15~18°C (Room Temp): Full-bodied Reds. (The cellar cool standard)

---

## [Exam Traps & Success Tips]

[Trap]: Remember, 'Room Temperature' in the wine world is NOT 25°C. It's the cool 15-18°C of a traditional cellar.
[Tip]: If it's sweet or has bubbles, it MUST be the coldest!

